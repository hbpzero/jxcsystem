package com.jxc.jxcsystem.pojo;

public class PurDetail {
    private String goodsNo;
    private String vendorNo;
    private String purtoNo;
    private Integer purdeId;
    private Integer purdeNum;
    private float purdeSub;

    public String getGoodsNo() {
        return goodsNo;
    }

    public void setGoodsNo(String goodsNo) {
        this.goodsNo = goodsNo;
    }

    public String getVendorNo() {
        return vendorNo;
    }

    public void setVendorNo(String vendorNo) {
        this.vendorNo = vendorNo;
    }

    public String getPurtoNo() {
        return purtoNo;
    }

    public void setPurtoNo(String purtoNo) {
        this.purtoNo = purtoNo;
    }

    public Integer getPurdeId() {
        return purdeId;
    }

    public void setPurdeId(Integer purdeId) {
        this.purdeId = purdeId;
    }

    public Integer getPurdeNum() {
        return purdeNum;
    }

    public void setPurdeNum(Integer purdeNum) {
        this.purdeNum = purdeNum;
    }

    public float getPurdeSub() {
        return purdeSub;
    }

    public void setPurdeSub(float purdeSub) {
        this.purdeSub = purdeSub;
    }
}
