package com.jxc.jxcsystem.pojo;

import java.util.Date;

public class Puttotal {
    private Integer puttoId;
    private String puttoNo;
    private Date puttoTime;
    private String puttoMan;
    private String puttoShiUnit;

    public Integer getPuttoId() {
        return puttoId;
    }

    public void setPuttoId(Integer puttoId) {
        this.puttoId = puttoId;
    }

    public String getPuttoNo() {
        return puttoNo;
    }

    public void setPuttoNo(String puttoNo) {
        this.puttoNo = puttoNo;
    }

    public Date getPuttoTime() {
        return puttoTime;
    }

    public void setPuttoTime(Date puttoTime) {
        this.puttoTime = puttoTime;
    }

    public String getPuttoMan() {
        return puttoMan;
    }

    public void setPuttoMan(String puttoMan) {
        this.puttoMan = puttoMan;
    }

    public String getPuttoShiUnit() {
        return puttoShiUnit;
    }

    public void setPuttoShiUnit(String puttoShiUnit) {
        this.puttoShiUnit = puttoShiUnit;
    }
}
